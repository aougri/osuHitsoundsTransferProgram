    public void initTransfer(String choice) {
        jCheckBox1.setEnabled(false);
        jCheckBox2.setEnabled(false);
        jCheckBox3.setEnabled(false);

        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        jCheckBox3.setSelected(false);

        switch (choice) {
            case "h":
                jRadioButton2.setEnabled(false);
                jRadioButton3.setEnabled(false);
                break;
            case "i":

                jRadioButton1.setEnabled(false);
                jRadioButton3.setEnabled(false);
                break;
            case "a":

                jRadioButton1.setEnabled(false);
                jRadioButton2.setEnabled(false);
                break;
        }

    }

    private void endTransfer() {
        jCheckBox1.setEnabled(true);
        jCheckBox2.setEnabled(true);
        jCheckBox3.setEnabled(true);

        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        jCheckBox3.setSelected(false);

        jRadioButton1.setEnabled(true);
        jRadioButton2.setEnabled(true);
        jRadioButton3.setEnabled(true);

    }

    public String checkForCheckedRDBtns() {
        String choice = "";
        if (jRadioButton1.isSelected()) {
            choice = "h";
            numberOfCheckedButtons++;
        } else if (jRadioButton2.isSelected()) {
            choice = "i";
            numberOfCheckedButtons++;
        } else if (jRadioButton3.isSelected()) {
            choice = "a";
            numberOfCheckedButtons++;
        }

        return choice;

    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        jfc.setDialogTitle("Choose the Source's file directory: ");
        jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        int returnValue = jfc.showSaveDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            if (jfc.getSelectedFile().isDirectory()) {
                System.out.println("You selected the directory: " + jfc.getSelectedFile());
                jLabel1.setText(jfc.getSelectedFile().getAbsolutePath());
                try {
                    directory.fileParserUnit.WriteToFile(srcFilePath,
                            jfc.getSelectedFile().getAbsolutePath());

                } catch (IOException ex) {
                    Logger.getLogger(OHSTP.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }// GEN-LAST:event_jButton1ActionPerformed

