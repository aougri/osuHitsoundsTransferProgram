    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String choice = checkForCheckedRDBtns();
        if (choice.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please select one of the Options.");
        } else {
            initTransfer(choice);

            String srcPath = directory.fileParserUnit.readOneLine(new File(srcFilePath));
            String trgtPath = directory.fileParserUnit.readOneLine(new File(trgtFilePath));

            if (JOptionPane.showConfirmDialog(rootPane,
                    "You are about to start the transfer from: " + srcPath + " to: " + trgtPath + " ") == 0) {

                jLabel3.setText("Truncating " + trgtPath);

                try {
                    directory.directoryHandlingUnit.truncate(trgtPath, choice);
                } catch (IOException ex) {
                    Logger.getLogger(OHSTP.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(rootPane, "An Error Occured " + ex.getMessage(), "Error Alert",
                            JOptionPane.ERROR_MESSAGE);
                }

                jCheckBox1.setSelected(true);

                jLabel3.setText("Finished Step 1, truncating has succeeded!");
                try {
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(OHSTP.class.getName()).log(Level.SEVERE, null, ex);
                }

                jLabel3.setText("Preparing 2nd Step: Moving the files.");

                jLabel3.setText("Moving files...");

                try {
                    directory.directoryHandlingUnit.MoveFiles(srcPath, trgtPath, choice);
                } catch (IOException ex) {
                    Logger.getLogger(OHSTP.class.getName()).log(Level.SEVERE, null, ex);
                }

                jCheckBox2.setSelected(true);

                jCheckBox3.setSelected(true);

                jLabel3.setText("Transfer has been a success!");

                JOptionPane.showMessageDialog(rootPane,
                        "The File transfer has been completed! Enjoy a new sound experience! (skill gain has been boosted ++)",
                        "Transfer Complete!", JOptionPane.INFORMATION_MESSAGE);

                endTransfer();

            }
        }

    }// GEN-LAST:event_jButton3ActionPerformed

